const JSZip = require('jszip');

function esc(s) {
  return String(s || '').replace(/&/g,'&amp;').replace(/</g,'&lt;').replace(/>/g,'&gt;').replace(/"/g,'&quot;');
}
function safe(s) {
  return String(s).replace(/[^a-z0-9]/gi,'_').replace(/_+/g,'_').slice(0,60);
}

async function buildEpub(title, author, html) {
  const id = 'kernl-' + Date.now();
  const t = esc(title), a = esc(author);

  const contentHtml = `<?xml version="1.0" encoding="UTF-8"?>
<!DOCTYPE html PUBLIC "-//W3C//DTD XHTML 1.1//EN" "http://www.w3.org/TR/xhtml11/DTD/xhtml11.dtd">
<html xmlns="http://www.w3.org/1999/xhtml">
<head><title>${t}</title>
<style>
body{font-family:Georgia,serif;font-size:1em;line-height:1.75;margin:1.5em 2em}
h1{font-size:1.8em;margin-bottom:.2em}
.byline{font-style:italic;color:#777;margin-bottom:2em}
h2{font-size:1.05em;font-weight:bold;margin:1.8em 0 .5em;padding-bottom:5px;border-bottom:1px solid #ddd;color:#8B4513}
p{margin:0 0 .9em;text-align:justify}
.footer{margin-top:3em;font-size:.8em;color:#aaa;font-style:italic}
</style>
</head>
<body>
<h1>${t}</h1>
<div class="byline">by ${a} \u2014 KERNL Summary</div>
${html}
<div class="footer">Generated by KERNL \u2014 for the curious</div>
</body>
</html>`;

  const opf = `<?xml version="1.0" encoding="UTF-8"?>
<package xmlns="http://www.idpf.org/2007/opf" unique-identifier="bid" version="2.0">
<metadata xmlns:dc="http://purl.org/dc/elements/1.1/">
<dc:title>${t} \u2014 KERNL</dc:title>
<dc:creator>${a}</dc:creator>
<dc:language>en</dc:language>
<dc:identifier id="bid">${id}</dc:identifier>
</metadata>
<manifest>
<item id="c" href="content.html" media-type="application/xhtml+xml"/>
<item id="ncx" href="toc.ncx" media-type="application/x-dtbncx+xml"/>
</manifest>
<spine toc="ncx"><itemref idref="c"/></spine>
</package>`;

  const ncx = `<?xml version="1.0"?>
<ncx xmlns="http://www.daisy.org/z3986/2005/ncx/" version="2005-1">
<head><meta name="dtb:uid" content="${id}"/></head>
<docTitle><text>${t}</text></docTitle>
<navMap><navPoint id="n1" playOrder="1">
<navLabel><text>Summary</text></navLabel>
<content src="content.html"/>
</navPoint></navMap>
</ncx>`;

  const container = `<?xml version="1.0"?>
<container version="1.0" xmlns="urn:oasis:names:tc:opendocument:xmlns:container">
<rootfiles><rootfile full-path="OEBPS/content.opf" media-type="application/oebps-package+xml"/></rootfiles>
</container>`;

  const zip = new JSZip();
  zip.file('mimetype', 'application/epub+zip');
  zip.folder('META-INF').file('container.xml', container);
  const oebps = zip.folder('OEBPS');
  oebps.file('content.opf', opf);
  oebps.file('toc.ncx', ncx);
  oebps.file('content.html', contentHtml);

  return await zip.generateAsync({ type: 'nodebuffer', mimeType: 'application/epub+zip' });
}

module.exports = async (req, res) => {
  if (req.method !== 'POST') return res.status(405).json({ error: 'Method not allowed' });

  const { title, author, html, kindleEmail } = req.body;
  if (!title || !html) return res.status(400).json({ error: 'Title and html are required' });
  if (!kindleEmail || !kindleEmail.includes('@')) return res.status(400).json({ error: 'Valid Kindle email required' });

  const RESEND_API_KEY = process.env.RESEND_API_KEY;
  if (!RESEND_API_KEY) return res.status(500).json({ error: 'Resend not configured' });

  try {
    // Build EPUB
    const epubBuffer = await buildEpub(title, author, html);
    const filename = safe(title) + '_KERNL.epub';
    const epubBase64 = epubBuffer.toString('base64');

    // Send via Resend
    const emailRes = await fetch('https://api.resend.com/emails', {
      method: 'POST',
      headers: {
        'Authorization': `Bearer ${RESEND_API_KEY}`,
        'Content-Type': 'application/json'
      },
      body: JSON.stringify({
        from: 'KERNL <kindle@kernlbooks.com>',
        to: [kindleEmail],
        subject: title + ' — KERNL Summary',
        text: `Your KERNL summary of "${title}" by ${author} is attached as an EPUB file. Open it on your Kindle to start reading.\n\nGenerated by KERNL — for the curious\nkernlbooks.com`,
        attachments: [
          {
            filename: filename,
            content: epubBase64
          }
        ]
      })
    });

    const emailData = await emailRes.json();
    if (!emailRes.ok) {
      console.error('Resend error:', emailData);
      return res.status(500).json({ error: emailData.message || 'Email failed to send' });
    }

    return res.status(200).json({ ok: true, id: emailData.id });

  } catch (err) {
    console.error('Send to Kindle error:', err.message);
    return res.status(500).json({ error: err.message || 'Failed to send to Kindle' });
  }
};
